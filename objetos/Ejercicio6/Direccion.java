public class Direccion{

    private String calle;
    private String ciudad;
    private int codigoPostal;
    private String pais;

    public Direccion(){



    }

    public Direccion(String calle,String ciudad,int codigoPostal,String pais){

        this.calle=calle;
        this.ciudad=ciudad;
        this.codigoPostal=codigoPostal;
        this.pais=pais;

    }

}